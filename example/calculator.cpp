/* APIs ----------------------------------------------------------------------*/
auto Add(int a, int b) -> int {
	return a + b;
}

auto Sub(int a, int b) -> int {
	return a - b;
}

auto Mul(int a, int b) -> int {
	return a * b;
}

auto Div(int a, int b) -> int {
	return a / b;
}
